
class Toado {
    double x,y;
    Toado (double x,double y){
        this.x=x;
        this.y=y;
    }
    public  void setx(double x) {
        this.x=x;
    }
    public  double getx() {
        return x;
    }
    public  void sety(double y) {
        this.y=y;
    }
    public  double gety() {
        return y;
    }
    
}
